// Event Listeners这里是回调，原本是input，在输入的时候，会回调re.callback，现在改成，吧enableEditingItems（e）也回调过去

RE.editor.addEventListener("input", RE.enabledEditingItems);
RE.editor.addEventListener("keyup", function(e) {
    var KEY_LEFT = 37, KEY_RIGHT = 39;
    if (e.which == KEY_LEFT || e.which == KEY_RIGHT) {
        RE.enabledEditingItems();
    }
});



